export function generateSEO(treatment: string, practice: Practice) {
  const slug = treatment.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/(^-|-$)/g, '');
  const locSlug = practice.location
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/(^-|-$)/g, '');

  return {
    title: `${treatment} Before & After | ${practice.location}`,
    altText: `${treatment} before and after results at ${practice.name} in ${practice.location}`,
    metaDescription: `See real, verified ${treatment.toLowerCase()} before and after photos from ${practice.name} in ${practice.location}. Results shown with patient consent.`,
    url: `${practice.website}/before-after/${slug}-${locSlug}`,
    filename: `before-after-${slug}-${locSlug}.jpg`,
  };
}

export function buildChainOfCustody(session: Session): ChainOfCustody {
  const checkpoints = [
    {
      id: 'capture',
      icon: '📷',
      label: 'Captured in-app',
      detail: 'Photo taken via Provē capture flow',
      timestamp: session.capturedAt,
      hash: `${session.captureHash.slice(0, 8)}…${session.captureHash.slice(-4)}`,
      verified: true,
    },
    {
      id: 'signed',
      icon: '🔐',
      label: 'Cryptographically signed',
      detail: 'SHA-256 recorded at capture',
      timestamp: session.signedAt,
      hash: `${session.signHash.slice(0, 8)}…${session.signHash.slice(-4)}`,
      verified: true,
    },
    {
      id: 'geo',
      icon: '📍',
      label: 'Geo-tagged & timestamped',
      detail: `${MOCK_PRACTICE.location} (${session.captureCoordinates.lat.toFixed(2)}°N, ${Math.abs(session.captureCoordinates.lng).toFixed(2)}°W)`,
      timestamp: session.capturedAt,
      hash: null,
      verified: true,
    },
    {
      id: 'consent',
      icon: '✍️',
      label: 'Consent recorded',
      detail: session.consentTier
        ? `${session.consentTier === 'full' ? 'Fully visible' : session.consentTier === 'partial' ? 'Targeted obscuring' : session.consentTier === 'full_blur' ? 'Full blur' : 'Declined'} · ${session.consentSignatureSvg ? 'Patient signature on file' : 'No signature'}`
        : 'Pending consent',
      timestamp: session.consentAt ?? '',
      hash: null,
      verified: Boolean(session.consentTier),
    },
    {
      id: 'published',
      icon: '🌐',
      label: 'Published to web',
      detail: session.publishedAt
        ? 'Widget + Provē Gallery + sitemap updated'
        : 'Not yet published',
      timestamp: session.publishedAt ?? '',
      hash: session.publishHash
        ? `${session.publishHash.slice(0, 8)}…${session.publishHash.slice(-4)}`
        : null,
      verified: session.status === 'published',
    },
  ];

  return {
    sessionId: session.id,
    checkpoints,
    allVerified: checkpoints.every((checkpoint) => checkpoint.verified),
  };
}
